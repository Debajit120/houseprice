    private void registerbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerbuttonActionPerformed
        // TODO add your handling code here:
        if(this.uname.getText().equals("") || this.email.getText().equals("") || this.password.getText().equals("") ||  this.malfe.getSelectedIndex()==0)
        {
            this.r_result.setVisible(true);
            this.r_result.setText("invalid");
        }
        else 
        {
            try {
                if(this.conpass.getText().equals(this.password.getText()))
                {
                     int check=useralreadyexist();
                if(check==0)
                {
                this.r_result.setVisible(true);
                this.r_result.setText("already exist");
                }
                else
                {
                  String name,email,pass,malefe;  
                  name=this.uname.getText();
                  email=this.email.getText();
                  pass=this.password.getText();
                  malefe=this.malfe.getSelectedItem().toString();
                  registeruser(email+","+pass+","+name+","+malefe);
                  new NewJFrame(name).setVisible(true);
                  this.setVisible(false);
                }    
                
                }
                else
                {
                    this.r_result.setVisible(true);
                    this.r_result.setText("invalid password");
                    
                }    
               
            } catch (IOException ex) {
                Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
    }//GEN-LAST:event_registerbuttonActionPerformed

