    public int useralreadyexist()  throws IOException
    {
        File myObj = new File("user.txt");
      Scanner myReader = new Scanner(myObj);
      
      while (myReader.hasNextLine()) {
        String data = myReader.nextLine();
       // String str = "Hello,I'm,yourString";
       String[] splited = data.split(",");
       if(this.email.getText().equals(splited[0]))
           this.r_result.setVisible(false);
           return 0;
           
      }
    new NewJFrame(this.uname.getText()).setVisible(true);
           this.setVisible(false);  
    return 1;
    }
    public void registeruser(String s) throws IOException
     {
     FileWriter myWriter = new FileWriter("user.txt",true);
     BufferedWriter b=new BufferedWriter(myWriter);
     this.r_result.setText("registerd");
      b.write(s);
      b.newLine();
      b.close();
     }
    

